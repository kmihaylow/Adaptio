import { useState } from "react";

/** Малка "?" подсказка за непознати метрики (изискване т.10). */
export default function InfoTip({ text }: { text: React.ReactNode }) {
  const [open, setOpen] = useState(false);
  return (
    <>
      <button type="button" className="tip-btn" onClick={() => setOpen(!open)} aria-label="Какво е това?">
        ?
      </button>
      {open && <div className="tip-pop" onClick={() => setOpen(false)}>{text}</div>}
    </>
  );
}

